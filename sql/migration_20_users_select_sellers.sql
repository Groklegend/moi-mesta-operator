-- ============================================================
-- Миграция 20: видимость списка менеджеров для оператора
-- ============================================================
-- Оператор в кабинете «Заявка Плюс» (operator-leads.js) должен видеть
-- список менеджеров, чтобы выбрать кому передать лид. По умолчанию RLS
-- на public.users разрешает чтение только админу/коммерческому/себе.
--
-- Добавляем узкую SELECT-политику: любой залогиненный читает строки
-- юзеров с ролью 'seller'. Email и ФИО менеджеров — не секрет внутри
-- компании, а альтернатива (Worker endpoint) — лишний слой.

drop policy if exists "users select sellers" on public.users;
create policy "users select sellers" on public.users for select
  using (roles @> array['seller']::text[]);
