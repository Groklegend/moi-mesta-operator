-- ============================================================
-- Миграция 21: переименование leads.caller_phone → leads.called_phone
-- ============================================================
-- Семантика поменялась: в форме «Заявка Плюс» вместо «номер, с которого
-- звонил оператор» теперь спрашиваем «номер, на который звонил оператор»
-- (часто это общий номер компании/секретаря, отличается от личного
-- телефона ЛПР). Имя поля приводим в соответствие.

alter table public.leads rename column caller_phone to called_phone;
